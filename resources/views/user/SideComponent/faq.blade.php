  <div class="faq-section" id="what-is-jobads">
    <h2>What is Jobads?</h2>
    <p>Jobads is an online recruitment system used by employers to list their vacancies online and handle online applications from jobseekers. Jobads completes and automates the entire recruitment cycle, providing an electronic 'back-office' to make recruitment easy, fast, and efficient.</p>
    <a href="#top" class="top-link">Back to Top</a>
  </div>

  <div class="faq-section" id="benefit-from-jobads">
    <h2>How can I benefit from using Jobads?</h2>
    <p>If you are a job seeker looking for your first job or considering a career change, then Jobads is perfect for you! You can browse or search for vacancies and apply online immediately. Your application will be sent directly to the HR team of Jobads-registered employers, who will contact you if you're shortlisted.</p>
    <p>If you are an employer, Jobads provides a comprehensive electronic recruitment platform for online vacancy advertising and automating the recruitment process in a paperless environment. Employers get a dedicated Jobads homepage to list vacancies and share information about working for their company.</p>
    <a href="#top" class="top-link">Back to Top</a>
  </div>

  <div class="faq-section" id="register-to-jobads">
    <h2>How do I register to Jobads?</h2>
    <p>Jobseeker registration is free. You can register easily by clicking 'Sign Up' on the homepage. Once registered, you can use your account to apply for jobs at registered companies, submit separate profiles to different companies, and monitor the status of your applications.</p>
    <a href="#top" class="top-link">Back to Top</a>
  </div>

  <div class="faq-section" id="apply-multiple-vacancies">
    <h2>Can I apply for more than one vacancy in a company?</h2>
    <p>Yes, you can apply for any number of vacancies advertised by a company.</p>
    <a href="#top" class="top-link">Back to Top</a>
  </div>

  <div class="faq-section" id="view-application-status">
    <h2>Once I apply for a vacancy can I view the status of my application?</h2>
    <p>Yes, you can view the status of your application once submitted. The system will show the current stage of your application, such as "Applied," "Shortlisted," "Offered the Job," or "Not Successful."</p>
    <a href="#top" class="top-link">Back to Top</a>
  </div>

  <div class="faq-section" id="edit-profile">
    <h2>Can I edit my profile on a later date?</h2>
    <p>Yes, you can edit your profile at any time by logging into your account. After logging in, select the profile you wish to edit and make the necessary changes.</p>
    <a href="#top" class="top-link">Back to Top</a>
  </div>

  <div class="faq-section" id="upload-cv-photo">
    <h2>Can I upload my CV and my photograph along with my profile?</h2>
    <p>Yes, you can upload your CV in various formats such as .doc, .pdf, .rtf, .txt, .jpg, .gif, .tif, or .png. You can also upload your photograph in .jpg, .gif, .tif, or .png format.</p>
    <a href="#top" class="top-link">Back to Top</a>
  </div>

  <div class="faq-section" id="employer-application-view">
    <h2>How does the employer see my information and my application?</h2>
    <p>Employers see your application along with your uploaded CV, cover letter, and other documents. Filling out your profile is highly recommended as it helps employers evaluate your application and compare it with other candidates.</p>
    <a href="#top" class="top-link">Back to Top</a>
  </div>

